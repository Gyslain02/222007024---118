public class operators {
    public static void main(String args[])
    {
        int y=15;
        boolean a=true;
        boolean b=false;
        y++;
        System.out.println(y);
        ++y;
        System.out.println(y);
        y--;
        System.out.println(y);
        --y;
        System.out.println(y);
        System.out.println(y++ + ++y);
        System.out.println(--y - y--);
        System.out.println(~y);
        System.out.println(!a);
        System.out.println(!b);

    }
}

